def _get_pub_types(article: dict) -> Optional[List[str]]:
    """Extract PubMed publication types from article venue field."""
    venue = article.get("venue") or ""
    if not venue:
        return None
    return [pt.strip() for pt in venue.split(";") if pt.strip()]


