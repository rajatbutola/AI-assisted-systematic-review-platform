# ── Cache helpers ──────────────────────────────────────────────────────────────

def _ck(review_id, pmid, task):
    return f"ai_{review_id}_{pmid}_{task}"

def _db_task(review_id, task):
    """Namespace task key by review_id to prevent cross-review contamination."""
    return f"r{review_id}_{task}"

def _get_cached(review_id, pmid, task):
    # 1. Check session_state first (fastest)
    val = st.session_state.get(_ck(review_id, pmid, task))
    if val is not None:
        return val
    # 2. Fall back to DB — use namespaced task key
    raw = ai_repo.get_analysis(pmid, _db_task(review_id, task))
    if raw is None:
        return None
    # 3. Reconstruct Python object from stored dict
    obj = _deserialise(task, raw)
    if obj is not None:
        st.session_state[_ck(review_id, pmid, task)] = obj
    return obj

def _set_cached(review_id, pmid, task, value):
    st.session_state[_ck(review_id, pmid, task)] = value
    # Persist to DB with namespaced key
    try:
        from dataclasses import asdict
        serialisable = asdict(value) if hasattr(value, '__dataclass_fields__') else value
        ai_repo.save_analysis(pmid, _db_task(review_id, task), serialisable)
    except Exception as e:
        logger.warning("Could not persist AI result to DB (task=%s): %s", task, e)

def _clear_task_cache(review_id, articles, task):
    for a in articles:
        key = _ck(review_id, a["pmid"], task)
        if key in st.session_state:
            del st.session_state[key]
        # Also clear from DB
        try:
            from storage.database import get_connection
            with get_connection() as conn:
                conn.execute(
                    "DELETE FROM ai_analyses WHERE pmid=? AND task=?",
                    (a["pmid"], _db_task(review_id, task)))
        except Exception:
            pass

def _deserialise(task: str, raw: Any) -> Any:
    """Reconstruct a dataclass object from a stored dict."""
    if not isinstance(raw, dict):
        return raw
    try:
        if task == "data_pool":
            from pipeline.data_pooler import StudyData
            return StudyData.from_dict(raw)
        if task == "rob2":
            from pipeline.rob2_assessor import RoB2Assessment, RoB2Domain
            def _rob2_domain(d):
                if not isinstance(d, dict):
                    return RoB2Domain(name="Unknown")
                return RoB2Domain(
                    name=d.get("name", ""),
                    judgement=d.get("judgement", "no_information"),
                    signals=d.get("signals", []),
                    rationale=d.get("rationale", ""),
                )
            return RoB2Assessment(
                d1_randomisation = _rob2_domain(raw.get("d1_randomisation", {})),
                d2_deviations    = _rob2_domain(raw.get("d2_deviations", {})),
                d3_missing_data  = _rob2_domain(raw.get("d3_missing_data", {})),
                d4_measurement   = _rob2_domain(raw.get("d4_measurement", {})),
                d5_reporting     = _rob2_domain(raw.get("d5_reporting", {})),
                overall          = raw.get("overall", "no_information"),
                overall_rationale= raw.get("overall_rationale", ""),
                notes            = raw.get("notes", ""),
            )
        if task in ("nos", "nos_cohort", "nos_cc", "nos_xs"):
            from pipeline.nos_assessor import NOSAssessment, NOSDomain
            def _nos_domain(d, default_name=""):
                if not isinstance(d, dict):
                    return NOSDomain(name=default_name)
                return NOSDomain(
                    name=d.get("name", default_name),
                    stars=d.get("stars", 0),
                    max_stars=d.get("max_stars", 0),
                    items=d.get("items", []),
                    rationale=d.get("rationale", ""),
                )
            return NOSAssessment(
                instrument   = raw.get("instrument", ""),
                selection    = _nos_domain(raw.get("selection", {}),    "Selection"),
                comparability= _nos_domain(raw.get("comparability", {}),"Comparability"),
                outcome      = _nos_domain(raw.get("outcome", {}),      "Outcome / Exposure"),
                total_stars  = raw.get("total_stars", 0),
                max_stars    = raw.get("max_stars", 9),
                quality_grade= raw.get("quality_grade", "poor"),
                notes        = raw.get("notes", ""),
            )
        if task in ("classify", "study_class"):
            from pipeline.study_classifier import StudyClassification
            return StudyClassification(**raw)
        if task in ("pico", "s1_pico"):
            from pipeline.pico_extractor import PICOResult
            return PICOResult(**raw)
        # summarize, grade — return raw dict/string as-is
        return raw
    except Exception as e:
        logger.warning("_deserialise failed for task=%s: %s", task, e)
        return None


# ── DOI / source link helper ───────────────────────────────────────────────────

def _article_links_md(article: dict) -> str:
    """Return HTML link badges for an article."""
    links = []
    doi = article.get("doi") or ""
    if doi:
        doi_clean = doi.strip()
        for prefix in ("https://doi.org/", "http://doi.org/", "doi:"):
            if doi_clean.lower().startswith(prefix.lower()):
                doi_clean = doi_clean[len(prefix):]
                break
        links.append(
            f'<a href="https://doi.org/{doi_clean.strip()}" target="_blank" ' +
            'class="sr-article-links"><span class="sr-link-doi">⊞ DOI</span></a>'
        )

    source = str(article.get("source") or "")
    pmid   = article.get("pmid") or ""
    if source == "pubmed" and pmid and not pmid.startswith("EPMC:"):
        links.append(
            f'<a href="https://pubmed.ncbi.nlm.nih.gov/{pmid}/" target="_blank">' +
            '<span class="sr-link-pubmed">PubMed ↗</span></a>'
        )

    url = article.get("url") or ""
    if "europepmc" in url:
        links.append(
            f'<a href="{url}" target="_blank">' +
            '<span class="sr-link-epmc">Europe PMC ↗</span></a>'
        )

    if not links:
        return ""
    return ('<div class="sr-article-links" style="display:flex;flex-wrap:wrap;gap:0.35rem;margin-top:0.4rem;">' +
            "".join(links) + "</div>")


def _render_article_links(article: dict) -> None:
    """Render styled article link badges via st.markdown."""
    html = _article_links_md(article)
    if html:
        st.markdown(html, unsafe_allow_html=True)


# ── Consensus logic ────────────────────────────────────────────────────────────

def _get_consensus_decision(review_id, pmid, stage="title_abstract"):
    adj = adjudication_repo.get_adjudication(review_id, pmid, stage=stage)
    if adj and adj.get("final_decision"):
        return adj["final_decision"]
    all_decs = screening_repo.get_all_decisions_for_article(
        review_id, pmid, stage)
    primary = {k: v for k, v in all_decs.items() if k not in SYSTEM_REVIEWER_IDS}
    if not primary:
        return None
    unique = set(primary.values())
    return unique.pop() if len(unique) == 1 else None

def _get_included_articles(review_id, all_articles):
    """Return Stage 2 included articles if Stage 2 has started, else Stage 1.
    Uses same adjudication-priority consensus logic as get_stage2_counts.
    """
    s2_counts = article_repo.get_stage2_counts(review_id)
    has_stage2 = (s2_counts.get("s2_included", 0) +
                  s2_counts.get("s2_excluded", 0)) > 0

    if has_stage2:
        # Mirror exactly what get_stage2_counts does — adjudication first,
        # then consensus — so the count here matches the dashboard metric.
        s2_pmids = article_repo.get_stage1_included_pmids(review_id)
        s2_included_pmids = set()
        for pmid in s2_pmids:
            dec = _get_consensus_decision(review_id, pmid, stage="full_text")
            if dec == "include":
                s2_included_pmids.add(pmid)
        return [a for a in all_articles if a["pmid"] in s2_included_pmids]

    # Fall back to Stage 1 included
    return [a for a in all_articles
    
            if _get_consensus_decision(review_id, a["pmid"]) == "include"]


def _count_pending(review_id, all_articles):
    count = 0
    for a in all_articles:
        all_decs = screening_repo.get_all_decisions_for_article(
            review_id, a["pmid"], "title_abstract"
        )
        primary = {k: v for k, v in all_decs.items() if k not in SYSTEM_REVIEWER_IDS}
        if len(primary) == 0:
            count += 1
    return count


