    if not all_articles:
        st.info("📋 No articles found. Run a search first (Search tab).")
        return

    included  = _get_included_articles(review_id, all_articles)
    n_total   = len(all_articles)
    n_incl    = len(included)
    n_pending = _count_pending(review_id, all_articles)
    db_counts = article_repo.get_screening_counts(review_id)
    n_conflict= db_counts.get("conflict", 0)

    c1, c2, c3, c4 = st.columns(4)
    c1.metric("Total Articles", n_total)
    c2.metric("✅ Included",    n_incl)
    c3.metric("⏳ Pending",     n_pending)
    c4.metric("⚠️ Conflicts",   n_conflict)

    if n_pending > 0:
        st.warning(f"⚠️ **{n_pending}** article(s) not yet screened. "
                   "Complete screening first.")
    if n_conflict > 0:
        st.warning(f"⚠️ **{n_conflict}** conflict(s) unresolved. "
                   "Switch to **Editor** → Screening → Conflicts to resolve.")

    if n_incl == 0:
        st.info("No articles included yet. Go to **Screening** and mark articles as Include.")
        return

    st.markdown(f"""
    <div style="display:flex;align-items:center;gap:0.75rem;
                background:var(--emerald-light,#D1FAE5);
                border:1px solid rgba(5,150,105,0.25);
                border-left:3px solid #059669;
                border-radius:10px;padding:0.75rem 1rem;margin-bottom:1rem;">
        <span style="font-size:1.1rem;">✓</span>
        <div>
            <span style="font-weight:600;color:#065F46;font-size:0.9rem;">
                {n_incl} article{'s' if n_incl != 1 else ''} included
            </span>
            <span style="color:#047857;font-size:0.82rem;margin-left:0.5rem;">
                · Meta-analysis preparation · PICO/Summary available in Screening tabs
            </span>
        </div>
    </div>
    """, unsafe_allow_html=True)
    st.divider()

    st.info(
        "💡 **PICO Extraction** is available per-article in **Stage 1 Screening**. "
        "**Full-text Summarisation** is available per-article in **Stage 2 Screening**. "
        "This tab focuses on post-inclusion meta-analysis preparation."
    )

    (tab_pool, tab_rob, tab_grade) = st.tabs([
        "📊 Data Pooling",
        "⚖️ Risk of Bias",
        "📋 GRADE / SoF",
    ])

    with tab_pool:
        _tab_data_pool(review_id, included)
    with tab_rob:
        _tab_risk_of_bias(review_id, included)
    with tab_grade:
        _tab_grade(review_id, included)


