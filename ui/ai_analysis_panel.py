def _render_traffic_light(review_id, articles):
    """
    Cochrane-style traffic light plot using Plotly.
    Rows = bias domains, Columns = studies (abbreviated titles).
    Circles coloured: green=low, yellow=some concerns, red=high, grey=N/A.
    """
    import plotly.graph_objects as go

    # Collect data matrix
    ROB2_DOMAINS = [
        "Randomisation", "Deviations", "Missing data",
        "Outcome measure", "Reporting"
    ]
    NOS_DOMAINS = ["Selection", "Comparability", "Outcome/Exposure"]
    ALL_DOMAINS = ["Overall"] + ROB2_DOMAINS + NOS_DOMAINS

    COLOR_MAP = {
        "low":            "#2E7D32",
        "some_concerns":  "#F9A825",
        "high":           "#C62828",
        "good":           "#2E7D32",
        "fair":           "#F9A825",
        "poor":           "#C62828",
        "na":             "#9E9E9E",
        "no_information": "#9E9E9E",
        None:             "#E0E0E0",
    }
    SYMBOL_MAP = {
        "low": "+", "some_concerns": "?", "high": "−",
        "good": "+", "fair": "?", "poor": "−",
        "na": "?", "no_information": "?", None: "",
    }

    studies   = []
    matrix    = []   # list of {domain: (color, symbol)}

    for a in articles:
        rob2 = _get_cached(review_id, a["pmid"], "rob2")
        nos  = _get_cached(review_id, a["pmid"], "nos")
        if not rob2 and not nos:
            continue

        # Abbreviate title to 15 chars + year
        yr    = str(a.get("year",""))[-2:]
        auths = (a.get("authors") or ["?"])
        first_author = (auths[0].split()[-1] if auths and auths[0] else "?")[:12]
        label = f"{first_author} '{yr}"
        studies.append(label)

        row = {}
        if rob2:
            row["Overall"] = (rob2.overall, None)
            for dom in rob2.domains:
                key = dom.name.split(":")[0].strip()
                short = next((d for d in ROB2_DOMAINS if d.lower() in key.lower()), key[:15])
                row[short] = (dom.judgement, None)
        elif nos:
            overall_key = {"good":"low","fair":"some_concerns","poor":"high"}.get(
                nos.quality_grade, "na")
            row["Overall"] = (overall_key, None)
            for dom_name, dom_obj in [
                ("Selection",          nos.selection),
                ("Comparability",      nos.comparability),
                ("Outcome/Exposure",   nos.outcome),
            ]:
                stars = dom_obj.stars if hasattr(dom_obj, "stars") else 0
                mx    = dom_obj.max_stars if hasattr(dom_obj, "max_stars") else 3
                rating = "low" if stars >= mx*0.75 else "some_concerns" if stars >= mx*0.5 else "high"
                row[dom_name] = (rating, None)

        matrix.append(row)

    if not studies:
        st.info("Run Risk of Bias assessment first to generate the traffic light plot.")
        return

    # Determine domains actually present
    present_domains = []
    for d in ALL_DOMAINS:
        if any(d in row for row in matrix):
            present_domains.append(d)

    n_studies = len(studies)
    n_domains = len(present_domains)

    fig = go.Figure()

    for di, domain in enumerate(present_domains):
        for si, (study, row) in enumerate(zip(studies, matrix)):
            rating, _ = row.get(domain, (None, None))
            color  = COLOR_MAP.get(rating, "#E0E0E0")
            symbol = SYMBOL_MAP.get(rating, "")

            # Circle as scatter marker
            fig.add_trace(go.Scatter(
                x=[si], y=[n_domains - 1 - di],
                mode="markers+text",
                marker=dict(
                    symbol="circle",
                    size=28,
                    color=color,
                    line=dict(color="white", width=1.5),
                ),
                text=symbol,
                textfont=dict(color="white", size=13, family="Arial Black"),
                textposition="middle center",
                hovertemplate=f"<b>{study}</b><br>{domain}: {rating or 'Not assessed'}<extra></extra>",
                showlegend=False,
            ))

    # Legend entries
    for label, color, sym in [
        ("Low risk / Good",   "#2E7D32", "+"),
        ("Some concerns / Fair", "#F9A825", "?"),
        ("High risk / Poor",  "#C62828", "−"),
        ("Not assessed",      "#E0E0E0", ""),
    ]:
        fig.add_trace(go.Scatter(
            x=[None], y=[None],
            mode="markers",
            marker=dict(size=12, color=color,
                        line=dict(color="white", width=1)),
            name=label, showlegend=True,
        ))

    fig.update_layout(
        height=max(320, 60 * n_domains + 80),
        margin=dict(l=160, r=20, t=40, b=80),
        plot_bgcolor="white",
        paper_bgcolor="white",
        legend=dict(orientation="h", yanchor="bottom", y=-0.25,
                    xanchor="center", x=0.5),
        xaxis=dict(
            tickmode="array",
            tickvals=list(range(n_studies)),
            ticktext=studies,
            tickangle=-45,
            showgrid=False, zeroline=False,
            side="top",
        ),
        yaxis=dict(
            tickmode="array",
            tickvals=list(range(n_domains)),
            ticktext=list(reversed(present_domains)),
            showgrid=False, zeroline=False,
            autorange=False,
            range=[-0.5, n_domains - 0.5],
        ),
        title=dict(text="Risk of Bias - Traffic Light Plot", x=0.5,
                   font=dict(size=14, family="Arial")),
    )

    st.plotly_chart(fig, use_container_width=True, key=f"rob_tl_{review_id}")

    # PNG download via kaleido
    c1, c2 = st.columns(2)
    with c1:
        try:
            import kaleido  # noqa: F401 — check availability silently
            png = fig.to_image(format="png", scale=3,
                               width=max(600, n_studies*70+200))
            st.download_button("📥 Download PNG (high-res)",
                               data=png,
                               file_name=f"rob_traffic_light_{review_id}.png",
                               mime="image/png", use_container_width=True)
        except Exception:
            st.info("💡 Install kaleido for PNG/SVG export: "
                    "`pip install kaleido`", icon="ℹ️")
    with c2:
        try:
            import kaleido  # noqa: F401
            svg = fig.to_image(format="svg",
                               width=max(600, n_studies*70+200))
            st.download_button("📥 Download SVG (vector)",
                               data=svg,
                               file_name=f"rob_traffic_light_{review_id}.svg",
                               mime="image/svg+xml", use_container_width=True)
        except Exception:
            pass  # already shown above


